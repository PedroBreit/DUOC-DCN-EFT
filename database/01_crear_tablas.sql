CREATE TABLE CURSOS (
    ID_CURSO NUMBER
        GENERATED BY DEFAULT AS IDENTITY,

    NOMBRE VARCHAR2(120 CHAR) NOT NULL,

    DESCRIPCION VARCHAR2(500 CHAR) NOT NULL,

    INSTRUCTOR VARCHAR2(120 CHAR) NOT NULL,

    CUPO_MAXIMO NUMBER(10) NOT NULL,

    ACTIVO NUMBER(1) DEFAULT 1 NOT NULL,

    FECHA_CREACION TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,

    CONSTRAINT PK_CURSOS
        PRIMARY KEY (ID_CURSO),

    CONSTRAINT CK_CURSOS_CUPO_MAXIMO
        CHECK (CUPO_MAXIMO > 0),

    CONSTRAINT CK_CURSOS_ACTIVO
        CHECK (ACTIVO IN (0, 1))
);


CREATE TABLE INSCRIPCIONES (
    ID_INSCRIPCION NUMBER
        GENERATED BY DEFAULT AS IDENTITY,

    ID_ESTUDIANTE NUMBER NOT NULL,

    NOMBRE_ESTUDIANTE VARCHAR2(120 CHAR) NOT NULL,

    CORREO_ESTUDIANTE VARCHAR2(150 CHAR) NOT NULL,

    ID_CURSO NUMBER NOT NULL,

    ESTADO VARCHAR2(30 CHAR) DEFAULT 'CONFIRMADA' NOT NULL,

    FECHA_INSCRIPCION TIMESTAMP
        DEFAULT CURRENT_TIMESTAMP NOT NULL,

    CONSTRAINT PK_INSCRIPCIONES
        PRIMARY KEY (ID_INSCRIPCION),

    CONSTRAINT UK_INS_EST_CURSO
        UNIQUE (ID_ESTUDIANTE, ID_CURSO),

    CONSTRAINT CK_INSCRIPCIONES_ESTADO
        CHECK (
            ESTADO IN (
                'PENDIENTE',
                'CONFIRMADA',
                'CANCELADA',
                'RECHAZADA'
            )
        ),

    CONSTRAINT FK_INSCRIPCIONES_CURSO
        FOREIGN KEY (ID_CURSO)
        REFERENCES CURSOS (ID_CURSO)
);


CREATE INDEX IDX_INSCRIPCIONES_ESTUDIANTE
    ON INSCRIPCIONES (ID_ESTUDIANTE);


CREATE INDEX IDX_INSCRIPCIONES_CURSO
    ON INSCRIPCIONES (ID_CURSO);


CREATE INDEX IDX_INSCRIPCIONES_CORREO
    ON INSCRIPCIONES (CORREO_ESTUDIANTE);