CREATE TABLE EXAMENES (
    ID_EXAMEN NUMBER
        GENERATED BY DEFAULT AS IDENTITY,

    ID_CURSO NUMBER NOT NULL,

    TITULO VARCHAR2(150 CHAR) NOT NULL,

    PREGUNTA VARCHAR2(1000 CHAR) NOT NULL,

    RESPUESTA_CORRECTA VARCHAR2(1000 CHAR) NOT NULL,

    PUNTAJE_MAXIMO NUMBER(5, 2) DEFAULT 100 NOT NULL,

    ACTIVO NUMBER(1) DEFAULT 1 NOT NULL,

    FECHA_CREACION TIMESTAMP
        DEFAULT CURRENT_TIMESTAMP NOT NULL,

    CONSTRAINT PK_EXAMENES
        PRIMARY KEY (ID_EXAMEN),

    CONSTRAINT FK_EXAMENES_CURSO
        FOREIGN KEY (ID_CURSO)
        REFERENCES CURSOS (ID_CURSO),

    CONSTRAINT CK_EXAMENES_PUNTAJE
        CHECK (PUNTAJE_MAXIMO > 0),

    CONSTRAINT CK_EXAMENES_ACTIVO
        CHECK (ACTIVO IN (0, 1))
);


CREATE INDEX IDX_EXAMENES_CURSO
    ON EXAMENES (ID_CURSO);


CREATE TABLE CALIFICACIONES (
    ID_CALIFICACION NUMBER
        GENERATED BY DEFAULT AS IDENTITY,

    ID_EXAMEN NUMBER NOT NULL,

    ID_ESTUDIANTE NUMBER NOT NULL,

    RESPUESTA_ESTUDIANTE VARCHAR2(1000 CHAR) NOT NULL,

    PUNTAJE NUMBER(5, 2) DEFAULT 0 NOT NULL,

    FECHA_RESPUESTA TIMESTAMP
        DEFAULT CURRENT_TIMESTAMP NOT NULL,

    CONSTRAINT PK_CALIFICACIONES
        PRIMARY KEY (ID_CALIFICACION),

    CONSTRAINT FK_CALIF_EXAMEN
        FOREIGN KEY (ID_EXAMEN)
        REFERENCES EXAMENES (ID_EXAMEN),

    CONSTRAINT UK_CALIF_EXAMEN_EST
        UNIQUE (ID_EXAMEN, ID_ESTUDIANTE),

    CONSTRAINT CK_CALIF_PUNTAJE
        CHECK (PUNTAJE >= 0)
);


CREATE INDEX IDX_CALIF_EXAMEN
    ON CALIFICACIONES (ID_EXAMEN);


CREATE INDEX IDX_CALIF_ESTUDIANTE
    ON CALIFICACIONES (ID_ESTUDIANTE);